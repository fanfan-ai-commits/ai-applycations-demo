function createTangDenseLinks() {
  const names = tang.map(([name]) => name);
  const links = [];
  const sinkOnly = new Set([
    '戴叔伦',
    '司空曙',
    '顾况',
    '杜荀鹤',
    '张祜',
    '许浑',
    '王翰',
    '王湾',
    '常建',
    '祖咏',
    '包融',
    '綦毋潜',
    '丘为',
    '崔国辅',
    '皇甫冉',
    '皇甫曾',
    '殷遥',
    '崔曙',
    '薛逢',
    '郑谷',
    '韩偓',
    '司空图',
  ]);
  const coreNames = names.filter((n) => !sinkOnly.has(n));

  const pushStepLinks = (step, arr) => {
    for (let i = 0; i < arr.length; i++) {
      const a = arr[i];
      const b = arr[(i + step) % arr.length];
      links.push([a, b]);
    }
  };

  // 核心层高密连接：整体更炸，但不把外围叶子点编进强互连
  pushStepLinks(1, coreNames);
  pushStepLinks(2, coreNames);
  pushStepLinks(4, coreNames);
  pushStepLinks(7, coreNames);

  const hubs = ['李白', '杜甫', '王维', '白居易', '韩愈', '李商隐'];
  coreNames.forEach((name, i) => {
    hubs.forEach((hub, j) => {
      if (name !== hub && (i + j) % 6 === 0) {
        links.push([hub, name]);
      }
    });
  });

  // 外围孤点：仅有入边，不向外发边（方向语义上为“被连接”）
  const sinkLinks = {
    戴叔伦: ['钱起', '刘长卿'],
    司空曙: ['钱起', '卢纶'],
    顾况: ['白居易', '刘禹锡'],
    杜荀鹤: ['罗隐', '韦庄'],
    张祜: ['杜牧', '李商隐'],
    许浑: ['李商隐', '温庭筠'],
    王翰: ['王之涣', '王昌龄'],
    王湾: ['张若虚', '崔颢'],
    常建: ['孟浩然', '王维'],
    祖咏: ['王维', '储光羲'],
    包融: ['张若虚'],
    綦毋潜: ['王维'],
    丘为: ['王维'],
    崔国辅: ['王昌龄'],
    皇甫冉: ['钱起'],
    皇甫曾: ['钱起'],
    殷遥: ['高适'],
    崔曙: ['王之涣'],
    薛逢: ['李商隐'],
    郑谷: ['韦庄'],
    韩偓: ['温庭筠'],
    司空图: ['罗隐'],
  };
  Object.entries(sinkLinks).forEach(([target, sources]) => {
    sources.forEach((s) => links.push([s, target]));
  });

  return links;
}

const rawLinks = [...curatedLinks, ...createTangDenseLinks()];

function buildNodes() {
  const nodes = [];
  let id = 0;
  for (const [name, weight] of tang) {
    nodes.push({ id: String(id++), name, dynasty: 'tang', weight });
  }
  for (const [name, weight] of song) {
    nodes.push({ id: String(id++), name, dynasty: 'song', weight });
  }
  return nodes;
}

const nameToId = new Map();
export const allNodes = buildNodes();
allNodes.forEach((n) => nameToId.set(n.name, n.id));

const seenLinks = new Set();
export const allLinks = rawLinks
  .map(([a, b]) => {
    const s = nameToId.get(a);
    const t = nameToId.get(b);
    if (s == null || t == null) return null;
    return { source: s, target: t };
  })
  .filter(Boolean)
  .filter((l) => l.source !== l.target)
  .filter((l) => {
    const key =
      l.source < l.target
        ? `${l.source}::${l.target}`
        : `${l.target}::${l.source}`;
    if (seenLinks.has(key)) return false;
    seenLinks.add(key);
    return true;
  });

// 韩愈–贾岛：贾岛未在列表则跳过（上面已过滤 null）
export function getGraph() {
  return { nodes: allNodes, links: allLinks };
}
